def fetch_table_paginated(name, batch_size=50000):
    all_dfs = []
    offset = 0

    while True:
        url = f"https://aws-event-api.bitopro.com/{name}?limit={batch_size}&offset={offset}"
        r = requests.get(url)
        r.raise_for_status()
        data = r.json()

        if len(data) == 0:
            print(f"{name}: no more rows, stop at offset={offset}")
            break

        df_batch = pd.DataFrame(data)
        all_dfs.append(df_batch)

        print(f"{name}: fetched {len(df_batch)} rows, offset={offset}")
        offset += batch_size
        time.sleep(0.2)

    final_df = pd.concat(all_dfs, ignore_index=True)
    print(f"{name}: final shape = {final_df.shape}")
    return final_df

