"""
## 6. 跨表資金流向特徵 (Cross-table Fund Flow)

分析資金在不同交易類型間的流動模式
"""

def calculate_fund_flow_patterns(twd_transfer, crypto_transfer, usdt_twd_trading, usdt_swap):
    """計算跨表資金流向特徵"""
    
    # 計算每個 user 在各表的首次和最後交易時間
    twd_time = twd_transfer.groupby("user_id")["created_at"].agg(["min", "max"]).reset_index()
    twd_time.columns = ["user_id", "twd_first", "twd_last"]
    
    crypto_time = crypto_transfer.groupby("user_id")["created_at"].agg(["min", "max"]).reset_index()
    crypto_time.columns = ["user_id", "crypto_first", "crypto_last"]
    
    trade_time = usdt_twd_trading.groupby("user_id")["updated_at"].agg(["min", "max"]).reset_index()
    trade_time.columns = ["user_id", "trade_first", "trade_last"]
    
    swap_time = usdt_swap.groupby("user_id")["created_at"].agg(["min", "max"]).reset_index()
    swap_time.columns = ["user_id", "swap_first", "swap_last"]
    
    # 合併時間資訊
    flow_feat = twd_time.merge(crypto_time, on="user_id", how="outer")
    flow_feat = flow_feat.merge(trade_time, on="user_id", how="outer")
    flow_feat = flow_feat.merge(swap_time, on="user_id", how="outer")
    
    # 計算整體活動時間範圍
    time_cols = ["twd_first", "crypto_first", "trade_first", "swap_first"]
    flow_feat["overall_first_time"] = flow_feat[time_cols].min(axis=1)
    
    time_cols = ["twd_last", "crypto_last", "trade_last", "swap_last"]
    flow_feat["overall_last_time"] = flow_feat[time_cols].max(axis=1)
    
    flow_feat["overall_active_span_sec"] = (
        flow_feat["overall_last_time"] - flow_feat["overall_first_time"]
    ).dt.total_seconds()
    
    # 計算快速資金流模式（24小時內的跨表操作）
    # 例如：TWD 入金後 24 小時內有 crypto 出金
    flow_feat["twd_to_crypto_gap_sec"] = (
        flow_feat["crypto_first"] - flow_feat["twd_first"]
    ).dt.total_seconds()
    
    flow_feat["crypto_to_twd_gap_sec"] = (
        flow_feat["twd_first"] - flow_feat["crypto_first"]
    ).dt.total_seconds()
    
    # 標記快速流轉（< 24 小時）
    flow_feat["is_fast_twd_to_crypto"] = (
        (flow_feat["twd_to_crypto_gap_sec"] > 0) & 
        (flow_feat["twd_to_crypto_gap_sec"] < 86400)
    ).astype(int)
    
    flow_feat["is_fast_crypto_to_twd"] = (
        (flow_feat["crypto_to_twd_gap_sec"] > 0) & 
        (flow_feat["crypto_to_twd_gap_sec"] < 86400)
    ).astype(int)
    
    # 計算交易類型多樣性
    flow_feat["has_twd"] = flow_feat["twd_first"].notna().astype(int)
    flow_feat["has_crypto"] = flow_feat["crypto_first"].notna().astype(int)
    flow_feat["has_trade"] = flow_feat["trade_first"].notna().astype(int)
    flow_feat["has_swap"] = flow_feat["swap_first"].notna().astype(int)
    flow_feat["txn_type_diversity"] = (
        flow_feat["has_twd"] + flow_feat["has_crypto"] + 
        flow_feat["has_trade"] + flow_feat["has_swap"]
    )
    
    # 只保留數值特徵
    numeric_cols = ["user_id", "overall_active_span_sec", "twd_to_crypto_gap_sec", 
                    "crypto_to_twd_gap_sec", "is_fast_twd_to_crypto", "is_fast_crypto_to_twd",
                    "has_twd", "has_crypto", "has_trade", "has_swap", "txn_type_diversity"]
    
    flow_feat = flow_feat[numeric_cols].fillna(0)
    
    return flow_feat

