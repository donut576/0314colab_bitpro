## 1. 網絡關係特徵 (Network Features)



def build_network_features(crypto_transfer):
    """建立轉帳網絡特徵"""
    
    # 基礎網絡統計
    network_out = crypto_transfer[crypto_transfer["has_relation_user"] == 1].groupby("user_id").agg(
        network_out_degree=("relation_user_id", "nunique"),  # 轉給多少不同的人
        network_out_txn_count=("id", "count"),
        network_out_total_value=("twd_value", "sum"),
        network_out_avg_value=("twd_value", "mean")
    ).reset_index()
    
    # 入度：計算有多少人轉給這個 user
    network_in = crypto_transfer[crypto_transfer["has_relation_user"] == 1].groupby("relation_user_id").agg(
        network_in_degree=("user_id", "nunique"),  # 從多少不同的人收到
        network_in_txn_count=("id", "count"),
        network_in_total_value=("twd_value", "sum"),
        network_in_avg_value=("twd_value", "mean")
    ).reset_index()
    network_in = network_in.rename(columns={"relation_user_id": "user_id"})
    
    # 合併出度與入度
    network_feat = network_out.merge(network_in, on="user_id", how="outer").fillna(0)
    
    # 計算網絡特徵比例
    network_feat["network_out_in_degree_ratio"] = network_feat["network_out_degree"] / (network_feat["network_in_degree"] + 1)
    network_feat["network_out_in_value_ratio"] = network_feat["network_out_total_value"] / (network_feat["network_in_total_value"] + 1)
    network_feat["network_total_degree"] = network_feat["network_out_degree"] + network_feat["network_in_degree"]
    network_feat["network_degree_imbalance"] = abs(network_feat["network_out_degree"] - network_feat["network_in_degree"]) / (network_feat["network_total_degree"] + 1)
    
    # 計算二階網絡（朋友的朋友）- 簡化版
    # 找出每個 user 的直接連接對象
    edges = crypto_transfer[crypto_transfer["has_relation_user"] == 1][["user_id", "relation_user_id"]].drop_duplicates()
    
    # 計算共同連接數（三角形結構）
    edge_counts = edges.groupby("user_id").size().reset_index(name="network_direct_connections")
    network_feat = network_feat.merge(edge_counts, on="user_id", how="left").fillna(0)
    
    return network_feat

