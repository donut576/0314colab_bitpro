def analyze_ip_patterns(twd_transfer, crypto_transfer, usdt_twd_trading):
    """分析 IP 使用模式"""
    
    # 合併所有表的 IP 資訊
    all_ips = pd.concat([
        twd_transfer[["user_id", "source_ip_hash"]].assign(source="twd"),
        crypto_transfer[["user_id", "source_ip_hash"]].assign(source="crypto"),
        usdt_twd_trading[["user_id", "source_ip_hash"]].assign(source="trade")
    ]).dropna(subset=["source_ip_hash"])
    
    # 計算每個 IP 被多少 user 使用
    ip_user_count = all_ips.groupby("source_ip_hash")["user_id"].nunique()
    
    # 定義共用 IP：被超過 3 個 user 使用
    shared_ips = ip_user_count[ip_user_count >= 3].index
    
    # 標記共用 IP
    all_ips["is_shared_ip"] = all_ips["source_ip_hash"].isin(shared_ips).astype(int)
    
    # 聚合到 user level
    ip_feat = all_ips.groupby("user_id").agg(
        total_unique_ips=("source_ip_hash", "nunique"),
        total_ip_usage_count=("source_ip_hash", "count"),
        shared_ip_count=("is_shared_ip", "sum"),
        shared_ip_ratio=("is_shared_ip", "mean"),
        ip_source_diversity=("source", "nunique")  # 在多少種交易類型中使用 IP
    ).reset_index()
    
    # IP 跳動率（IP 數量 / 交易次數）
    ip_feat["ip_jump_rate"] = ip_feat["total_unique_ips"] / (ip_feat["total_ip_usage_count"] + 1e-9)
    
    # 計算每個 user 在不同交易類型中的 IP 重疊度
    user_ip_by_source = all_ips.groupby(["user_id", "source"])["source_ip_hash"].apply(set).reset_index()
    
    # 計算 IP 一致性（跨交易類型使用相同 IP 的程度）
    def calc_ip_consistency(group):
        if len(group) <= 1:
            return 1.0
        sets = group["source_ip_hash"].tolist()
        intersection = set.intersection(*sets)
        union = set.union(*sets)
        return len(intersection) / (len(union) + 1e-9) if len(union) > 0 else 0
    
    ip_consistency = user_ip_by_source.groupby("user_id").apply(calc_ip_consistency).reset_index(name="ip_consistency_score")
    ip_feat = ip_feat.merge(ip_consistency, on="user_id", how="left").fillna(1.0)
    
    return ip_feat

