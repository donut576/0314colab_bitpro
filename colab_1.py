"""
## 4. 時間序列異常模式 (Temporal Anomaly Detection)

檢測異常的時間模式，如集中在特定時段的交易
"""

def extract_temporal_anomalies(twd_transfer, crypto_transfer, usdt_twd_trading, usdt_swap):
    """提取時間序列異常特徵"""
    
    temporal_feats = []
    
    # 對每種交易類型計算時間熵
    for df, prefix, time_col in [
        (twd_transfer, "twd", "created_at"),
        (crypto_transfer, "crypto", "created_at"),
        (usdt_twd_trading, "trade", "updated_at"),
        (usdt_swap, "swap", "created_at")
    ]:
        # 計算每小時交易分布的熵
        def calc_hour_entropy(hours):
            if len(hours) == 0:
                return 0
            counts = hours.value_counts(normalize=True)
            entropy = -sum(p * np.log(p + 1e-9) for p in counts)
            return entropy
        
        hour_entropy = df.groupby("user_id")["hour"].apply(calc_hour_entropy).reset_index(name=f"{prefix}_hour_entropy")
        
        # 計算星期分布的熵
        day_entropy = df.groupby("user_id")["day_of_week"].apply(calc_hour_entropy).reset_index(name=f"{prefix}_day_entropy")
        
        # 合併
        temp_feat = hour_entropy.merge(day_entropy, on="user_id", how="outer")
        temporal_feats.append(temp_feat)
    
    # 合併所有時間特徵
    temporal_feat = temporal_feats[0]
    for feat in temporal_feats[1:]:
        temporal_feat = temporal_feat.merge(feat, on="user_id", how="outer")
    
    temporal_feat = temporal_feat.fillna(0)
    
    # 計算整體時間集中度（低熵 = 高度集中在特定時段）
    entropy_cols = [col for col in temporal_feat.columns if "entropy" in col]
    temporal_feat["avg_time_entropy"] = temporal_feat[entropy_cols].mean(axis=1)
    temporal_feat["min_time_entropy"] = temporal_feat[entropy_cols].min(axis=1)
    
    return temporal_feat

print("Extracting temporal anomalies...")
temporal_feat = extract_temporal_anomalies(twd_transfer, crypto_transfer, usdt_twd_trading, usdt_swap)
