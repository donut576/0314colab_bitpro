def calc_time_gap_features(df, user_col, time_col, prefix):
    """
    對每個 user 計算相鄰交易間隔（秒）的統計特徵
    """
    tmp = df[[user_col, time_col]].dropna().sort_values([user_col, time_col]).copy()
    tmp["gap_sec"] = tmp.groupby(user_col)[time_col].diff().dt.total_seconds()

    gap_feat = tmp.groupby(user_col).agg(
        **{
            f"{prefix}_gap_mean_sec": ("gap_sec", "mean"),
            f"{prefix}_gap_std_sec": ("gap_sec", "std"),
            f"{prefix}_gap_min_sec": ("gap_sec", "min"),
            f"{prefix}_gap_max_sec": ("gap_sec", "max"),
        }
    ).reset_index()

    # burstiness = (std - mean) / (std + mean)
    gap_feat[f"{prefix}_burstiness"] = (
        (gap_feat[f"{prefix}_gap_std_sec"].fillna(0) - gap_feat[f"{prefix}_gap_mean_sec"].fillna(0)) /
        (gap_feat[f"{prefix}_gap_std_sec"].fillna(0) + gap_feat[f"{prefix}_gap_mean_sec"].fillna(0) + 1e-9)
    )

    return gap_feat


def calc_active_day_features(df, user_col, date_col, prefix):
    """
    每個 user 的活躍天數
    """
    feat = df.groupby(user_col).agg(
        **{
            f"{prefix}_active_days": (date_col, "nunique")
        }
    ).reset_index()
    return feat

